	<p><b>Notes to Editors</b></p>

	<p>1. The Player Auction will be held in the Mysore Hall, ITC Gardenia, Bengaluru, on 12 February 2014 (and on 13 February 2014, if required). The proceedings will commence at 9:30 am sharp on 12 February 2014.</p>

	<p>2. No media will be permitted into the auction room but a media conference area (Magnolia Hall) will be provided at the ITC Gardenia to accommodate members of the media.</p>

	<p>3. Media conferences with the IPL Chairman and / or Franchise members will take place during the auction. The schedule for these conferences will be advised on the day.</p>

