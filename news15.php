	<p>6. The BCCI policy pertaining to Media Accreditation, will be followed while approving / rejecting the applications. Accordingly, there will be a restriction on the number of mediapersons per organisation who can be accredited. Representatives of websites, radio stations, electronic news agencies and production houses will not be accredited. The News Access Guidelines for covering the event, are part of the accreditation link. The Media Accreditation application form can be accessed after indicating one’s acceptance of the guidelines.</p>

	<p>7. Mediapersons, whose applications are approved, will receive an alert indicating the same. They will be able to collect an accreditation card, from the Media Registration Desk at the entrance of the ITC Gardenia, between 7:30 am and 9:30 am on Wednesday, 12 February 2014. It is mandatory that they bring along their organisation’s official photo-ID.</p>
