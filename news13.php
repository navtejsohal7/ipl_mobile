	<p>He propelled India to the top of the Test rankings in 2009, helped them lift the World Cup in 2011 and then made South Africa the best Test team in the world. Gary Kirsten has established himself as one of the best cricket coaches of all time. And now he has embarked on a new road in his coaching journey, joining the Delhi Daredevils as their head coach.</p>

	<p>The 2014 Player Auction was Kirsten’s christening as the coach of an IPL team and thus he was introduced to the riveting territory of world’s most popular Twenty20 tournament. The action in the auction room prepared Kirsten for the excitement in store when the tournament begins.</p>

	<p>Here’s what Kirsten told iplt20.com as he took charge of the Delhi Daredevils.</p>

	<p><b>Does this feel a bit like homecoming?</b></p>

	<p>It is actually and it’s nice to be back. I have some very fond memories of India from my three years with the Indian team. I always anticipated that I will be back here. I’ve enjoyed my time in this country. It is nice to be part of the IPL and be involved with Indian cricket again.</p>

	<p><b>How was the auction as a means to start your IPL journey?</b></p>

	<p>It was quite a weird process for me, it was an interesting experience and a very different one from those I have ever had. It was nice to be part of the action between sport and business. I am looking forward to working with a squad of players coming from different countries who all play their cricket very differently. I am looking forward to managing a team of different kinds of players.</p>

	<p><b>How did this appointment come about?</b></p>

	<p>It started with a phone call from my good friend Eric Simons, who was the head coach of Delhi Daredevils. He rang me up and asked what I would be doing after my contract with the Proteas ended. I said I’m not sure and he asked me if I would be interested in joining him at Delhi Daredevils. I said I would and that’s how the conversation began.</p>

	<p>A very crucial part of your coaching style is building a personal rapport with players and taking them into confidence. You will not have that time with a team that gathers for a few months in a year. How will that aspect change your coaching method?</p>

	<p>Yes, I will have to go about that part very quickly. One thing about the IPL is that it is a very intense competition and you have got to build a team culture. Yes, the time will be less but it can be done. I will have to go about it in a different way. The opportunity to create something within a team is exciting.</p>

	<p><b>How much will your experience with Team India help you manage an IPL team?</b></p>

	<p>It will make a huge difference. In fact, even with the Proteas we had players from five different cultures, so I am used to managing a varied dressing room and working in a cross culture atmosphere. I think it is very important that you understand what makes people tick in their country and how to get the best out of different individuals.</p>

	<p><b>How does the definition of the word coach change when you’re in charge of such a T20 team that plays only one tournament together a year?</b></p>

	<p>I don’t think it changes at all and I don’t think the word coach is a correct one for someone managing an international team. It’s more about managing people rather than coaching players.</p>

	<p><b>You teamed up with Paddy Upton when you were with India and South Africa and he must have helped you a great deal in understanding the mental make-up of the players. Will you miss having him by your side this time?</b></p>

	<p>I would miss him and yes, he has helped me a lot. But he had coaching aspirations and we agreed that it was a good time for us to move in different directions. We shared a great partnership for five years. I know he will add value to every dressing room. He will work with young coaches and help them go about their task. I was a young coach when I came to India and he played a crucial role in my development as a coach. He has given me a few pointers about how to manage an IPL team but he is an opposition now so he would not tell me everything.</p>

	<p><b>Will Eric Simons’s presence in the DD dressing room help you ease into your role?</b></p>

	<p>Yes, it was a factor behind my taking up the job. It is very important for me to have him around because he has been involved with the IPL for four years and he knows the requirements of the tournament well. I will be taking a lot of advice from him.</p>

	<p><b>How is the prospect of working closely with Kevin Pietersen?</b></p>

	<p>I am looking forward to working with him. He is a South African so the rapport will be easy to build. I have had chats with him and he was keen to continue playing for DD. We too wanted him to stay with us. He is a top-class player and we’re really pleased to have him.</p>
