	<p>One of the most intriguing things about sport is the fact is that it is united in its diversity. While the nature of one sport could be completely different from that of another, one can borrow various skill-sets from the other.</p>

	<p>A hockey player and a footballer can gain from adopting some training methods of one another’s sport pertaining to stamina, agility and awareness. In the same way, a cricketer can benefit by understanding and embracing a certain aspects of baseball and golf. Although the three sports are like chalk, cheese and chocolate, the basic of hitting the ball with a stick remains the same.</p>

	<p>International cricket teams have inculcated baseball-style fielding and throwing technique in their training for a decade now. Mike Young, a former American baseball player, has been involved with the Australian, South African and Indian teams in the past as their fielding mentor.</p>

	<p>However, it is an Indian cricketer, Ajinkya Rahane, and his Indian coach, Pravin Amre, who have now come up with the idea of adopting the baseball technique in batting.</p>

	<p>Amre, a former India batsman and the man who led Mumbai to three Ranji Trophy titles as their coach, has seen Rahane grow from a talented young man to the Test batsman he has become today. He is keen for his ward to establish himself as a complete batsman in all formats of the game.</p>

	<p>“One of the biggest challenges for batsmen today is to perform consistently in all three formats,” Amre told iplt20.com. “You have to work on every aspect of your batting – technique and power-hitting, temperament and shot-selection – if you don’t want to be pigeon-holed as a one format specialist.</p>

	<p>“I am proud that Ajinkya has proved his mettle with a Test century overseas (in Wellington, New Zealand). I think it’s now time for him to establish himself as a force to reckon with in T20 format, with the World Twenty20. So, I thought of introducing this baseball-style training to him.”</p>

	<p>With this idea, Amre has defied the notion that Indian cricket tends to look overseas for innovative coaching.</p>

	<p>“As a certified Level 3 coach, I get access to several websites that have information about coaching in various sports,” Amre said. “You can always take things from one sport and inculcate them in another with modifications to suit.</p>

	<p>“Cricket teams, for many years, have been using the baseball training technique in their fielding drills. Once, while going through one of these websites I thought, ‘if this technique can help in cricket fielding, why not batting? If it can help the fielders generate more power in their throws, why can’t it help the batsmen generate more power in their stroke?’”</p>

	<p>The question led to a few self-conducted experiments in the nets and when Amre was convinced that it worked, he thought of his special ward. While Rahane is tremendously skilled when it comes to timing and placing the ball, power-hitting doesn’t come naturally to him, owing to his skinny frame. Amre thought he had found a way for Ajinkya to generate optimum power in his shots using his existing physical strength.</p>

	<p>“I tried some of the things myself in the nets first before making Ajinkya do them,” Amre said. “I wouldn’t have tried it with any other batsman but I could freely involve Ajinkya because I know him so well and he trusts me completely as his coach. That trust is imperative because in the end it’s the player’s career that is on the line.</p>

	<p>“Ajinkya is not a very physically strong guy but he is wiry and he has an excellent sense of timing – his game is based on timing and technique. We worked on drills of movements that would enable him to generate more velocity on the ball.</p>

	<p>“One particular thing we worked on was using his entire core – in cricket while batting, we use mostly our shoulders, feet and hands. If he could learn to gather the strength of his core and put it into the stroke, the ball will go a distance. Even in the T20s, it’s not about how high you hit the ball but how long you hit it,” Amre explained.</p>

	<p>Rahane, who is in Bangladesh with Team India, elucidated the process of training further, saying the major work was in generating more powerful bat-swing while going for big strokes.</p>

	<p>“The objective of aligning to such a technique was to use my core to increase my productivity in hitting the ball,” Rahane told iplt20.com. “This was more a top-up to my existing strengths in the Twenty20 format.

	<p>“The focus was on to get my bat swing to an optimum level where I could consistently execute a range of shots. We have seen how baseball players use their core strength to get their swing right and we tried to take a leaf out of that and instill it into my training,” Rahane said.</p>

	<p>The mentor and the student put in hard yards for the limited number of days available to them in Mumbai to achieve the desired effect. “I had only three to four days with him before he left for Bangladesh,” Amre said. “We were fortunate to have fine indoor facilities at the BKC and MCA to carry out the training. We worked with bowling machines and wet artificial wickets.”</p>

	<p>While he facilitated Rahane with all the technical and physical knowhow, Amre’s foremost goal was to give Team India a batsman who believed he could bat the team to victory from any situation.</p>

	<p>“Most of all, I tried to instill a belief in him that he can play the big shots. In T20s it’s mostly about confidence and backing yourself to clear the rope.”</p>

	
